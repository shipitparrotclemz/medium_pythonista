# define an `int` variable named `tasty_seeds`. It contains an integer.
# the `:int` to the right of the variable name
# is an optional type-hint, telling us that the variable contains an int
tasty_seeds: int = 10

# add 2 seeds to tasty_seeds
# tasty_seeds = tasty_seeds + 2
tasty_seeds += 2

# pass tasty_seeds into print function, to print it to console
print(tasty_seeds)

# minus 2 seeds!
tasty_seeds -= 2
print(tasty_seeds)

# multiply the seeds by 4!
tasty_seeds *= 4
print(tasty_seeds)

# divide the seeds by 2!
tasty_seeds /= 2
print(tasty_seeds)
